// @lc code=start
function lengthOfLIS(nums: number[]): number {
  let dp = new Array(nums.length).fill(1);
  let maxLength = 1;
  for (let i = 0; i < nums.length; i++) {
    for (let j = 0; j < i; j++) {
      if (nums[j] < nums[i]) {
        dp[i] = Math.max(dp[j] + 1, dp[i]);
      }
    }
    maxLength = Math.max(maxLength, dp[i]);
  }
  return maxLength;
}
